	/**
	 * This only makes a sanity check of the test data
	 * @dataProvider provideSignature
	 */
	public function testValidity( $testString, $expectedSignature ) {
		$estimated = strlen( $testString ) - 2;
		$expected = array_reduce(
			$expectedSignature,
			function(  &$result , $item ) {
				$result += $item;
				return $result;
			},
			0
		);
		$this->assertTrue( $expected === $estimated, "Validity failed: $estimated - $expected");
	}

	/**
	 * @dataProvider provideSignature
	 */
	public function testSignture( $testString, $expectedSignature ) {
		$signature = FuzzyCompare::signature( $testString );
		$this->assertEquals( $expectedSignature, $signature, "Calculated signature failed\n" );
	}

	public function provideSignature() {
		return array_map( function( $a ) { return array( $a[0], $a[5] ); }, self::$testData );
	}

	/**
	 * @dataProvider provideSingleScore
	 */
	public function testSingleScore( $testString, $cmpString, $expectedFloor, $expectedCeil ) {
		$array = array_map( function( $a ) { return $a[0]; }, self::$testData );
		$fuzzy = new FuzzyCompare();
		$fuzzy->addStrings( $array );
		$fuzzy->compile();
		$score = $fuzzy->singleScore( $testString, $cmpString );
		$this->assertTrue( $expectedFloor <= $score && $score <= $expectedCeil, "Calculated single score failed: $score" );
	}

	public function provideSingleScore() {
		$cmpString = self::$testData[0][0];
		return array_map( function( $a ) use ( $cmpString ) { return array( $a[0], $cmpString, $a[1], $a[2] ); }, self::$testData );
	}

	/**
	 * @dataProvider provideOverallScore
	 */
	public function testOverallScore( $testString, $expectedFloor, $expectedCeil ) {
		$array = array_map( function( $a ) { return $a[0]; }, self::$testData );
		$fuzzy = new FuzzyCompare();
		$fuzzy->addStrings( $array );
		$fuzzy->compile();
		$score = $fuzzy->overallScore( $testString );
		$this->assertTrue( $expectedFloor <= $score && $score <= $expectedCeil, "Calculated overall score failed: $score" );
	}

	public function provideOverallScore() {
		return array_map( function( $a ) { return array( $a[0], $a[3], $a[4] ); }, self::$testData );
	}
